function detectLanguage() {
    const stored = localStorage.getItem(I18N_STORAGE_KEY);
    if (stored === 'zh' || stored === 'en') return stored;
    const navLang = (navigator.language || navigator.userLanguage || 'en').toLowerCase();
    return navLang.startsWith('zh') ? 'zh' : 'en';
}

let currentLang = detectLanguage();

function t(key, ...args) {
    let text = translations[currentLang]?.[key] || translations.zh[key] || key;
    if (args.length > 0) {
        for (let i = 0; i < args.length; i++) {
            text = text.replace(`{${i}}`, args[i]);
        }
    }
    return text;
}

function tp(key) {
    return translations[currentLang]?.providerNames?.[key] || translations.zh.providerNames?.[key] || key;
}

function ta(key) {
    return translations[currentLang]?.apiTypeNames?.[key] || translations.zh.apiTypeNames?.[key] || key;
}

function getLang() {
    return currentLang;
}

function setLang(lang) {
    if (lang !== 'zh' && lang !== 'en') return;
    currentLang = lang;
    localStorage.setItem(I18N_STORAGE_KEY, lang);
    applyTranslations();
}

function toggleLang() {
    setLang(currentLang === 'zh' ? 'en' : 'zh');
}

function applyTranslations() {
    document.documentElement.lang = currentLang === 'zh' ? 'zh-CN' : 'en';
    document.title = t('title');

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = t(key);
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = t(key);
    });

    const langBtn = document.getElementById('lang-switch-btn');
    if (langBtn) {
        langBtn.textContent = t('switchLang');
    }
    const loginLangBtn = document.getElementById('login-lang-switch-btn');
    if (loginLangBtn) {
        loginLangBtn.textContent = t('switchLang');
    }
}
